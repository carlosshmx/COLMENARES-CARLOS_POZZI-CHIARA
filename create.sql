DROP TABLE IF EXISTS ODONTOLOGOS; CREATE TABLE ODONTOLOGOS(ID LONG AUTO_INCREMENT PRIMARY KEY,MATRICULA INT NOT NULL,  NOMBRE VARCHAR(25) NOT NULL, APELLIDO VARCHAR(25) NOT NULL);

DROP TABLE IF EXISTS PACIENTES;
CREATE TABLE PACIENTES (ID LONG AUTO_INCREMENT PRIMARY KEY, NOMBRE VARCHAR(100) NOT NULL, APELLIDO VARCHAR(100) NOT NULL, DNI INT NOT NULL, FECHA DATE NOT NULL);

DROP TABLE IF EXISTS TURNOS;
CREATE TABLE TURNOS (ID LONG AUTO_INCREMENT PRIMARY KEY, PACIENTE_ID  LONG NOT NULL, ODONTOLOGO_ID LONG NOT NULL, FECHA DATE NOT NULL);

